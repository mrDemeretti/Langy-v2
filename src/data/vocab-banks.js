getWordsForLevel(level) {
    const order = ['A1','A2','B1','B2','C1','C2'];
    const idx = order.indexOf(level);
    let words = [];
    for (let i = 0; i <= idx; i++) {
        if (this[order[i]]) words = words.concat(this[order[i]].getAllWords());
    }
    return words;
},
